""" Install Python """
#   Download Python from the official website:
#       https://www.python.org/downloads/
#   Install Python:
#       - Check the box "Add Python to PATH"
#       - Select "Install Now"
#       - Select "Disable path length limit"
#   Finish the installation
#   Verify the installation:
#       - Open Command Prompt (cmd) or PowerShell
#       - Type "python" or "py"
#       - If Python is installed correctly, you will see the Python version and the Python prompt (>>>)

# ----------

""" Open Python """
#   Python Shell (Python Interactive Shell)
#       Open Python.exe
# OR
#   Open PowerShell as "administrator" (On Windows or VSCode)
#       Type "py", "python"
#           PS C:\Users\YourUserName> python or py
#               Interactive mode "ON"
      
# ----------

""" Check Python Version """
# >>> python --version
# version

# ----------

""" Clear the console """
# >>> clear
#   Clear the console

# ----------

""" Exit Python """
# >>> exit()
#       Exit Python.exe

# ----------

""" Create a Python Virtual Environment """
# python -m venv MY_VENV_PY 

#   Creates a folder named My_VENV_PY with the virtual environment inside.

""" Activate Python Virtual Environment """
# MY_VENV_PY\Scripts\activate.ps1 
         

""" Verify the Python Virtual Environment """
# >>> where python
#     python --version


""" Deactivate Python Virtual Environment """       
# >>> deactivate

