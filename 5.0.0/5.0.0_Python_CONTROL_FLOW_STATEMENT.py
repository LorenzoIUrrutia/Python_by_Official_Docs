# Control Flow Statements in Python
#  Control flow statements allow you to control the execution flow of your program based on conditions.
#   This includes loops and conditional statements.

# ----------

# While Loop
# A while loop repeatedly executes a block of code as long as a specified condition is True.

condition = True

while condition:
    # Code to execute while condition is True
    pass

# ----------

# If Statement
# An if statement executes a block of code if a specified condition is True.

condition = True
another_condition = False

if condition:
    # Code to execute if condition is True
    pass
elif another_condition:
    # Code to execute if another_condition is True
    pass
else:
    # Code to execute if both conditions are False
    pass

# ----------

# For Loop
# A for loop iterates over a sequence (like a list or a string) and executes a block of code for each item in the sequence.

sequence = [1, 2, 3, 4, 5]

for item in sequence:
    # Code to execute for each item
    pass

