""" 4.2.3_Python_NUM_FUNC_POW """

pow() # Raises a number to a specified power.

# ----------

# Integer powers
print(pow(2, 3))  # Output: 8
print(pow(3, 2))  # Output: 9

# Float powers
print(pow(2.5, 2))  # Output: 6.25
print(pow(3.1, 3))  # Output: 29.791

# Negative powers (reciprocal)
print(pow(2, -2))  # Output: 0.25
print(pow(3, -1))  # Output: 0.3333333333333333

# ----------
# ----------

""" (FOOTNOTE) """

pow() # As a function, it raises a number to a specified power.
# Assigning a power value to a variable
#  Can be used with integers and floats.
#   It returns the result of the exponentiation.

my_pow = pow(2, 3)  # Creates a variable with value 8
print(my_pow)  # Output: 8

