# ----------

float () # Converts a value to a float.

# ----------

# String to float conversion
print(float("42"))  # Output: 42.0
print(float("3.14"))  # Output: 3.14

# Integer to float conversion
print(float(42))  # Output: 42.0
print(float(-42))  # Output: -42.0

# Boolean to float conversion
print(float(True))  # Output: 1.0
print(float(False))  # Output: 0.0

# ----------
# ----------

""" (FOOTNOTE) """

float () # As a constructor, it creates a float object.
# Assigning a FLOAT value to a variable

my_float = float(3.14)  # Creates a float object with value 3.14
print(my_float)  # Output: 3.14

